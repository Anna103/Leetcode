package ArrayListExample;

import java.util.ArrayList;

public class ArrayListExample {

    public ArrayList myArrayListExample(){
        ArrayList<Integer> myArrayList = new ArrayList<>();
        myArrayList.add(1);
        myArrayList.add(11);
        myArrayList.add(41);
        myArrayList.add(2);
        myArrayList.add(6);
        myArrayList.add(-1);
        return myArrayList;

    }

    public ArrayList myArrayListExampleString(){
        ArrayList<String> myArrayList = new ArrayList<>();
        myArrayList.add("Anna");
        myArrayList.add("Jimmy");
        myArrayList.add("George");
        myArrayList.add("Mathew");
        return myArrayList;

    }
}

